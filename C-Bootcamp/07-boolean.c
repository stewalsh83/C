#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h> // true | false

int main(void)
{
    bool a = true;
    bool b = false;

    printf("a (true):   %d\n", a);
    printf("b (false):  %d\n", b);

    printf("\n");
    return 0;
}

// a, true,   on,   yes
// b, false,  off,  no

// booleans in c are integers %d